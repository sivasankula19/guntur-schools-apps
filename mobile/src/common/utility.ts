 export const transformListToGrid = (data:any) => {
  // debugger
    let currentMain = 0;
    const dayToIndex:any = {"MON":0,"TUE":1,"WED":2,"THU":3,"FRI":4,"SAT":5,"SUN":6}
    const emptyArr:any = [null, null, null, null, null, null, null]
    let finalOp = [[...emptyArr]]
    data.forEach((i:any) => {
      finalOp[currentMain][dayToIndex[i.dayShort]] = {...i};
      if(i.dayShort === "SUN"){
        finalOp.push([...emptyArr])
        currentMain++;
      }
    })
    return finalOp

  }
    


