  export const getDatesForMonth = (month:number, year:number) => {
    const dates = [];
    let date = new Date(year, month - 1, 1);
    while (date.getMonth() === month - 1) {
      const dayFull = date.toLocaleString('en-US', { weekday: 'long' });
      const dayShort = date.toLocaleString('en-US', { weekday: 'short' }).toUpperCase();
      const fullDate = `${String(date.getDate()).padStart(2, '0')}/${String(month).padStart(2, '0')}/${year}`;
      const formattedDate = `${month}/${date.getDate()}/${year}`;
      dates.push({
        id:date.getDate(),
        fullDate,
        date: formattedDate,
        day: dayFull,
        dayShort: dayShort,
        currentDay:date.getDate(),
        am:false,
        pm:false,
        isSchoolHoliday: dayShort==='SUN',
        attendanceMarked:0
      });
      date.setDate(date.getDate() + 1);
    }
    
    return dates;
  };

 export const transformListToGrid = (data:any) => {
    let currentMain = 0;
    const dayToIndex:any = {"MON":0,"TUE":1,"WED":2,"THU":3,"FRI":4,"SAT":5,"SUN":6}
    const emptyArr:any = [null, null, null, null, null, null, null]
    let finalOp = [[...emptyArr]]
    data.forEach((i:any) => {
      finalOp[currentMain][dayToIndex[i.dayShort]] = {...i};
      if(i.dayShort === "SUN"){
        finalOp.push([...emptyArr])
        currentMain++;
      }
    })
    return finalOp

  }


